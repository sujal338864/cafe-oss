import { Router } from 'express';
import { authenticate, authorize } from '../middleware/auth';
import { withTenantContext } from '../middleware/tenant';
import { MarketingService } from '../services/marketing.service';
import { GrowthService } from '../services/growth.service';
import { prisma } from '../common/prisma';
import rateLimit from 'express-rate-limit';
import { AuthRequest } from '../middleware/auth';

const router = Router();

// Apply auth & tenant context to all marketing routes
router.use(authenticate as any);
router.use(authorize('ADMIN', 'MANAGER', 'SUPER_ADMIN') as any);
router.use(withTenantContext as any);

/**
 * GET /api/marketing/brand
 * Get the AI Brand Profile for the shop
 */
router.get('/brand', async (req: AuthRequest, res: any) => {
  try {
    let profile = await (prisma as any).brandProfile.findUnique({
      where: { shopId: req.user!.shopId }
    });
    
    if (!profile) {
      profile = await (prisma as any).brandProfile.create({
        data: { shopId: req.user!.shopId }
      });
    }
    
    return res.json({ profile });
  } catch (err: any) {
    return res.status(500).json({ error: 'Failed to fetch brand profile' });
  }
});

/**
 * PUT /api/marketing/brand
 * Update the AI Brand Profile 
 */
router.put('/brand', async (req: AuthRequest, res: any) => {
  try {
    const { brandType, toneOfVoice, targetAudience, primaryColor, secondaryColor, socialLinks } = req.body;
    
    const profile = await (prisma as any).brandProfile.upsert({
      where: { shopId: req.user!.shopId },
      update: { brandType, toneOfVoice, targetAudience, primaryColor, secondaryColor, socialLinks },
      create: { 
        shopId: req.user!.shopId, 
        brandType, toneOfVoice, targetAudience, primaryColor, secondaryColor, socialLinks 
      }
    });

    return res.json({ success: true, profile });
  } catch (err: any) {
    return res.status(500).json({ error: 'Failed to update brand profile' });
  }
});

