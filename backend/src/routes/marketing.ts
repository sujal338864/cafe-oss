/**
 * POST /api/marketing/intel/force
 * Manually force AI Brain to generate intel right now
 */
router.post('/intel/force', async (req: AuthRequest, res: any) => {
  try {
    const today = new Date();
    today.setHours(0, 0, 0, 0);

    // Delete existing to force recreation
    await (prisma as any).dailyMarketingIntel.deleteMany({
      where: { shopId: req.user!.shopId, date: today }
    });

    const { MarketingService } = await import('../services/marketing.service');
    const intel = await MarketingService.generateDailyIntel(req.user!.shopId);
    
    if (!intel) return res.status(503).json({ error: 'AI service unavailable. Please try again later.' });

    const dbIntel = await (prisma as any).dailyMarketingIntel.create({
      data: {
        shopId: req.user!.shopId,
        date: today,
        planText: intel.text,
        keyFocus: intel.keyFocus,
        actionItems: intel.actionItems || []
      }
    });

    return res.json({ intel: dbIntel });
  } catch (err: any) {
    console.error(err);
    return res.status(500).json({ error: 'Failed to generate intel' });
  }
});

